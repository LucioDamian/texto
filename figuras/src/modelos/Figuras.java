package modelos;
/*
 * Clase que representa un figura geometrica
 * 
 * @version 1.0
 * 
 * @author Damian
 */
public abstract class Figuras {
	/**
	 * Devuelve el area de la figura, en metros cuadrado
	 * @return el area de la figura, en metros cuadrado
	 */
	public abstract double area();
	/**
	 * Devuelve el perimetro de la figura, en metros 
	 * @return el perimetro de la figura, en metros 
	 */
	public abstract double perimetro();
	
}
